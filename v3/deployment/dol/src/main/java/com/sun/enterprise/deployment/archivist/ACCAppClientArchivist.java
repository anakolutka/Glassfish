package com.sun.enterprise.deployment.archivist;

import com.sun.enterprise.deployment.ApplicationClientDescriptor;
import java.io.IOException;
import org.glassfish.api.deployment.archive.ReadableArchive;
import org.jvnet.hk2.annotations.Scoped;
import org.jvnet.hk2.annotations.Service;
import org.jvnet.hk2.component.PerLookup;

/**
 * Acts as an AppClientArchivist but without running the post-open 
 * ModuleContentValidator.  
 * <p>
 * This archivist primarily supports the need in the ACC to open the
 * generated facade archive.  This allows the ACC to load the DOL 
 * ApplicationClientDescriptor using the filled-in descriptor from the
 * generated facade archive.  (The ModuleContentValidation would fail because
 * the files it looks for are in the developer's original app client JAR, not
 * in the generated facade JAR.)
 *
 * @author tjquinn
 */
@Service
@Scoped(PerLookup.class)
public class ACCAppClientArchivist extends AppClientArchivist {

    /**
     * No-op.
     * <p>
     * Allows the ACC to use this archivist to open an archive without
     * triggering the module content validation.
     * 
     * @param archive
     */
    @Override
    protected void runValidations(ReadableArchive archive) {
    }
}
